const int N = 1e4+9;
vector<int>g[N];
bool vis[N];
int pr[N];
void dfs(int u){
    for(auto v : g[u]){
        if(!vis[v]){
            vis[v] = true;
            pr[v] = u;
            dfs(v);
        }
    }
}
 
void testCases (int tt)
{
    int n,m,x,y;
    cin>>n>>m>>x>>y;
    for(int i = 1;i <= n;i++){
        g[i].clear();
        vis[i] = false;
        pr[i] = i;
    }
    while(m--){
        int u,v;
        cin>>u>>v;
        g[u].push_back(v);
        g[v].push_back(u);
    }
    for(int i = 1;i <= n;i++){
        sort(all(g[i]));
    }
    vis[x] = true;
    dfs(x);
    int crr = y;
    vector<int>ans;
    // for(int i = 1;i <= n;i++){
    //     cout<<pr[i]<<" ";
    // }
    while(pr[crr] != crr){
        ans.push_back(crr);
        crr = pr[crr];
    }
    ans.push_back(x);
    for(int i = ans.size()-1; i >= 0;i--){
        cout<<ans[i]<<" ";
    }
    cout<<endl;
}
 
 
